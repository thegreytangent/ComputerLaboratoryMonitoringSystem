<script type="text/javascript">
  $(document).ready(function(){

    $('.lab4').click(function(){


      $.ajax({
        url:'laboratory/unit_info_get.php',
        method:'POST',
        dataType:'json',
        data:{lab:'lab4',unit:$(this).attr('id')},
        success:function(data)
        {
         $.map(data,function(i){
          $('#'+i.PartsName+'_select').find('option').get(0).remove();
          $('#'+i.PartsName+'_select').prepend('<option value="'+i.BrandId+'" selected>'+i.brand+'</option>');
          $('#'+i.PartsName+'_serial').val(i.SerialNumber);
          $('#'+i.PartsName+'-UnitId').val(i.Id);
        });
       }
     });

      

      $.ajax({
        url:'laboratory/unit_info_get_ip.php',
        method:'POST',
        dataType:'json',
        data:{lab:'lab4',unit:$(this).attr('id')},
        success:function(data)
        {
          $('#ipAddress').val(data.IpAddress);
          $('#labIPId').val(data.IPId);
        }
      });
      // $('#unit_info_form')[0].trigger('reset');


      $('#unit_info_modal').modal('show');

      $('#pc_id').html("").append('<input type="hidden" name="pc_id" value="'+$(this).attr('id')+'"/>');
      $('#pc_lab').html("").append('<input type="hidden" name="pc_lab" value="lab4 <?php echo LabUnits::active_lab4('pc-35'); ?>"/>');
    });


    $('#unit_info_modal').on('hidden.bs.modal', function () {
     location.reload();
   })


    function getInfo(data) {
     $('#'+data.PartsName+'_serial').val(data.PartsName);
   }




   $('#unit_info_form').submit(function(){
    $.ajax({
      url:'laboratory/unit_info_add.php',
      method:'POST',
      data:$(this).serialize(),
      success:function(res){
        if (res == 1) {
          alert("Records has been inserted");
          location.reload();
        }
      }
    });
    return false;
  });





 });
</script>
